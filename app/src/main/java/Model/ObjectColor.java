package Model;

public class ObjectColor {
    String R;
    String G;
    String B;
    public ObjectColor(String R, String G, String B){
        this.R = R;
        this.G = G;
        this.B = B;
    }
    public String getR(){return R;}
    public String getG(){return G;}
    public String getB(){return B;}
}
